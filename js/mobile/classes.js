        var redFlag = false;
        var color = null;
        var flag = document.getElementById("barFlag_" + this.signalId).value;

        var interval = helpers.getValueActualInterval( this.signalId, this.filteredValue);

        if (interval.HH && HH != '-') { redFlag = true; }
        if (interval.H && H != '-') { yellowFlag = true; }
        if (interval.L && L != '-') { yellowFlag = true; }
        if (interval.LL && LL != '-') { redFlag = true; }

        if (yellowFlag) { color = '#FFFF00'; if (parseInt(flag) < 1) {  document.getElementById("barFlag_" + this.signalId).value = 1; } }
        if (redFlag) { color = '#FF0000';  if (parseInt(flag) < 2) {  document.getElementById("barFlag_" + this.signalId).value = 2; } }
        if (!yellowFlag && !redFlag) { document.getElementById("barFlag_" + this.signalId).value = 0;  }

        if (('null' != color)) {  document.getElementById('barValue_' + this.signalId).setAttribute("style", "color: "+ color +";");  } else {   document.getElementById('barValue_' + this.signalId).style.color = ""; }

