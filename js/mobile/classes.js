        var H_Percentage = parseFloat( helpers.calculatePercentage (this.signalId, H));
        var L_Percentage = parseFloat( helpers.calculatePercentage (this.signalId, L));
        var LL_Percentage = parseFloat( helpers.calculatePercentage (this.signalId, LL));

        document.getElementById("limit_HH_"+this.signalId).style['flex-basis'] = HH_Percentage + '%';
        document.getElementById("limit_H_"+this.signalId).style['flex-basis'] = H_Percentage + '%';
